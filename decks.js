function displayDecks() {

  // creating individual tables with headers
  var tier0table = "<table id='tier0' class='decks'><tr><th>INV</th><th>NAME</th><th>TYPE</th><th>NOTES</th><th>LINK</th><th>ID</th></tr>";
  var tier1table = "<table id='tier1' class='decks'><tr><th>INV</th><th>NAME</th><th>TYPE</th><th>NOTES</th><th>LINK</th><th>ID</th></tr>";
  var tier2table = "<table id='tier2' class='decks'><tr><th>INV</th><th>NAME</th><th>TYPE</th><th>NOTES</th><th>LINK</th><th>ID</th></tr>";
  var tier3table = "<table id='tier3' class='decks'><tr><th>INV</th><th>NAME</th><th>TYPE</th><th>NOTES</th><th>LINK</th><th>ID</th></tr>";

  // Loop through the deck array
  for (var i in data.decks) {
  
    var output = "";
    // CHECKBOX COLUMN
    output +=
      "<tr>" +
      "<td>" +
      "<input type='checkbox'></input>" +

      // DECKNAME COLUMN
      "<td>" +
      data.decks[i].name +
      "</td>"

    // ARCHETYPE COLUMN

    if (data.decks[i].archetype === 'RUSH') {
      output += "<td class='archetyperush'>" + data.decks[i].archetype + "</td>"
    } else if (data.decks[i].archetype === 'MID') {
      output += "<td class='archetypemidrange'>" + data.decks[i].archetype + "</td>"
    } else if (data.decks[i].archetype === 'COMBO') {
      output += "<td class='archetypecombo'>" + data.decks[i].archetype + "</td>"
    } else if (data.decks[i].archetype === 'CONTROL') {
      output += "<td class='archetypecontrol'>" + data.decks[i].archetype + "</td>"
    } else if (data.decks[i].archetype === 'REVERSAL') {
      output += "<td class='archetypereversal'>" + data.decks[i].archetype + "</td>"
    } else if (data.decks[i].archetype === 'STEAL') {
      output += "<td class='archetypesteal'>" + data.decks[i].archetype + "</td>"
    } else if (data.decks[i].archetype === 'MARS') {
      output += "<td class='archetypemars'>" + data.decks[i].archetype + "</td>"
    } else if (data.decks[i].archetype === 'GROUPHUG') {
      output += "<td class='archetypegrouphug'>" + data.decks[i].archetype + "</td>"
    } else if (data.decks[i].archetype === 'JANK') {
      output += "<td class='archetypejank'>" + data.decks[i].archetype + "</td>"
    } else {
      output += "<td></td>"
      data.decks[i].archetype;
    }

    // NOTES COLUMN
    output +=
      "<td>" +
      data.decks[i].notes +
      "</td>" +
      "<td>" +
      data.decks[i].link +
      "</td>" +
      "<td>" +
      data.decks[i].id +
      "</td>" +
      "</tr>";

      if (data.decks[i].tier == '0') {
        tier0table += output;
      } else if (data.decks[i].tier == '1') {
        tier1table += output;
      } else if (data.decks[i].tier == '2') {
        tier2table += output;
      } else if (data.decks[i].tier == '3') {
        tier3table += output;
      }
  }

  // Output the data to the "tierZeroList" element
  document.getElementById("tierZeroList").innerHTML = tier0table;
  document.getElementById("tierOneList").innerHTML = tier1table;
  document.getElementById("tierTwoList").innerHTML = tier2table;
  document.getElementById("tierThreeList").innerHTML = tier3table;
}

// Load displayDecks on window load
window.onload = displayDecks;